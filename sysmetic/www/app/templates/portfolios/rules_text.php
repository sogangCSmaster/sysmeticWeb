<b>제 7 조 (이용계약의 종료)</b><br />
<ul class="rule_list">
    <li>1) 회원의 해지
        <ul>
            <li>(1) 회원은 언제든지 서비스에게 해지의사를 통지함으로써 이용계약을 해지할 수 있습니다.</li>
            <li>(2) 이용계약은 회원의 해지의사가 서비스에 도달한 때에 종료 됩니다.</li>
        </ul>
    </li>
    <li>2) 서비스의 해지
        <ul>
            <li>(1) 서비스는 다음과 같은 사유가 있는 경우, 이용계약을 해지할 수 있습니다. 이 경우 서비스는 회원에게 전자우편, 전화, 팩스<br /><i></i>기타의 방법을 통하여 해지사유를 밝혀 해지의사를 통지합니다. 다만 서비스는 해당 회원에게 사전에 해지사유에 대한 의견진술의<br /><i></i>기회를 부여 할 수 있습니다.
                <ul>
                    <li>① 제6조 제2항에서 정하고 있는 이용계약의 승낙거부사유가 있음이 확인된 경우</li>
                    <li>② 회원이 서비스나 다른 회원 기타 타인의 권리나 명예, 신용 기타 정당한 이익을 침해하는 행위를 한 경우</li>
                    <li>③ 기타 회원이 이 약관에 위배되는 행위를 하거나 이 약관에서 정한 해지사유가 발생한 경우</li>
                </ul>
            </li>
            <li>(2) 이용계약은 서비스가 해지의사를 회원에게 통지함으로써 종료됩니다. 이 경우 서비스가 해지의사를 회원이 등록한 전자우편주소<br /><i></i>로 발송하거나 서비스 게시판에 게시함으로써 통지에 갈음합니다.</li>
        </ul>
    <li>3) 이용계약의 종료와 관련하여 발생한 손해는 이용계약이 종료된 해당 회원이 책임을 부담하여야 하고 서비스는 일체의 책임을 지지<br /><i></i>않습니다.</li>
</ul>
<br /><br />

<b>제 8 조 (회원탈퇴 및 자격 상실)</b><br />
<ul class="rule_list">
    <li>1) 회원"은 언제든지 서비스 내 "계정삭제" 화면을 통하여 이용계약 해지 신청을 할 수 있으며, 서비스는 관련법 등이 정하는 바에 따라<br /><i></i>이를 즉시 처리하여야 합니다.</li>
    <li>2) 회원이 계약을 해지할 경우, 관련법 및 개인정보취급방침에 따라 서비스가 회원정보를 보유하는 경우를 제외하고는 해지 즉시 회원의<br /><i></i>개인정보는 소멸됩니다. 또한 회원의 아이디는 재사용을 할 수 없습니다.</li>
    <li>3) 회원이 계약을 해지하는 경우, 회원이 작성한 게시물 일체는 삭제되지 않습니다.</li>
</ul>
