<b> 제 11 조 (회원의 게시물)</b><br />
회원이 작성한 게시물에 대한 모든 권리 및 책임은 이를 게시한 회원에게 있으며, 서비스는 회원이 게시하거나 등록하는 서비스의 내용물이 다음 각 항에 해당한다고 판단되는 경우에 사전통지 없이 삭제할 수 있고, 이에 대하여 서비스는 어떠한 책임도 지지 않습니다.<br />
<ul class="rule_list">
    <li>1) 다른 회원 또는 제3자를 비방하거나 중상모략으로 명예를 손상시키는 내용인 경우</li>
    <li>2) 공공질서 및 미풍양속에 위반되는 내용일 경우</li>
    <li>3) 범죄적 행위에 결부된다고 인정되는 경우</li>
    <li>4) 서비스의 저작권, 제3자의 저작권 등 기타 권리를 침해하는 내용인 경우</li>
    <li>5) 회원이 사이트와 게시판에 음란물을 게재하거나 음란사이트를 링크하는 경우</li>
    <li>6) 서비스로부터 사전 승인 받지 아니한 상업광고, 판촉 내용을 게시하는 경우</li>
    <li>7) 해당 상품과 관련 없는 내용인 경우</li>
    <li>8) 정당한 사유 없이 서비스의 영업을 방해하는 내용을 기재하는 경우</li>
    <li>9) 기타 관계법령에 위반된다고 판단되는 경우</li>
</ul>
<br /><br />

