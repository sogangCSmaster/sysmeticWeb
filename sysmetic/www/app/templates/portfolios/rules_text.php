<b>제 13 조 (이용제한 등)</b><br />
<ul class="rule_list">
    <li>1) 서비스는 회원이 이 약관의 의무를 위반하거나 서비스의 정상적인 운영을 방해한 경우, 경고, 일시정지, 영구이용정지 등으로 서비스<br /><i></i>이용을 단계적으로 제한할 수 있습니다.</li>
    <li>2) 서비스는 전항에도 불구하고, "주민등록법"을 위반한 명의도용 및 결제도용, 전화번호 도용, "저작권법" 및 "컴퓨터프로그램보호법"을 <br /><i></i>위반한 불법프로그램의 제공 및 운영방해, "정보통신망법"을 위반한 불법통신 및 해킹, 악성프로그램의 배포, 접속권한 초과행위 등과<br /><i></i>같이 관련법을 위반한 경우에는 즉시 영구이용정지를 할 수 있습니다. 본 항에 따른 영구이용정지 시 서비스 이용을 통해 획득한 혜택<br /><i></i>등도 모두 소멸되며, 서비스는 이에 대해 별도로 보상하지 않습니다.</li>
    <li>3) 서비스는 회원이 계속해서 3개월 이상 로그인하지 않는 경우, 회원정보의 보호 및 운영의 효율성을 위해 이용을 제한할 수 있습니다.</li>
    <li>4) 본 조의 이용제한 범위 내에서 제한의 조건 및 세부내용은 서비스의 이용제한정책에서 정하는 바에 의합니다.</li>
    <li>5) 본 조에 따라 서비스 이용을 제한하거나 계약을 해지하는 경우에는 서비스는 제16조[회원에 대한 통지]에 따라 통지합니다.</li>
    <li>6) 회원은 본 조에 따른 이용제한 등에 대해 서비스가 정한 절차에 따라 이의신청을 할 수 있습니다. 이 때 이의가 정당하다고 서비스가<br /><i></i>인정하는 경우 즉시 서비스의 이용을 재개합니다.</li>
</ul>
<br /><br />

<b>제 14 조 (책임제한)</b><br />
<ul class="rule_list">
    <li>1) 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스 제공에 관한 책임이 면제됩니다.</li>
    <li>2) 회원의 귀책사유로 인한 서비스 이용의 장애에 대하여는 책임을 지지 않습니다.</li>
    <li>3) 회원이 서비스와 관련하여 게재한 정보, 자료, 사실의 신뢰도, 정확성 등의 내용에 관하여는 책임을 지지 않습니다.</li>
    <li>4) 회원 간 또는 회원과 제3자 상호간에 서비스를 매개로 하여 거래 등을 한 경우에는 책임이 면제됩니다.</li>
    <li>5) 무료로 제공되는 서비스 이용과 관련하여 관련법에 특별한 규정이 없는 한 책임을 지지 않습니다.</li>
    <li>6) 제3자가 서비스 내 화면 또는 링크된 웹사이트를 통하여 광고한 제품 또는 서비스의 내용과 품질에 대하여 감시할 의무 기타 어떠한<br /><i></i>책임도 지지 아니합니다.</li>
    <li>7) 서비스 및 서비스의 임직원 그리고 대리인은 다음과 같은 사항으로부터 발생하는 손해에 대해 책임을 지지 아니합니다.
        <ul>
            <li>(1) 회원 상태정보의 허위 또는 부정확성에 기인하는 손해</li>
            <li>(2) 그 성질과 경위를 불문하고 서비스에 대한 접속 및 서비스의 이용과정에서 발생하는 개인적인 손해</li>
            <li>(3) 서버에 대한 제3자의 모든 불법적인 접속 또는 서버의 불법적인 이용으로부터 발생하는 손해</li>
            <li>(4) 서버에 대한 전송 또는 서버로부터의 전송에 대한 제3자의 모든 불법적인 방해 또는 중단행위로부터 발생하는 손해</li>
            <li>(5) 제3자가 서비스를 이용하여 불법적으로 전송, 유포하거나 또는 전송, 유포되도록 한 모든 바이러스, 스파이웨어 및<br /><i></i>기타 악성 프로그램으로 인한 손해</li>
            <li>(6) 전송된 데이터의 오류 및 생략, 누락, 파괴 등으로 발생되는 손해</li>
            <li>(7) 회원간의 회원 상태정보 등록 및 서비스 이용 과정에서 발생하는 명예훼손 기타 불법행위로 인한 각종 민형사상 책임</li>
        </ul>
    </li>
</ul>
<br /><br />

<b> 제 15 조 (권리의 귀속)</b><br />
<ul class="rule_list">
    <li>1) 서비스에 대한 저작권 및 지적재산권은 서비스에 귀속됩니다. 단, 회원의 "게시물" 및 제휴계약에 따라 제공된 저작물은 제외합니다.</li>
    <li>2) 서비스가 제공하는 서비스의 디자인, 서비스가 만든 텍스트, 스크립트(script), 그래픽, 회원 상호간 전송 기능 등 서비스가 제공하는<br /><i></i>서비스에 관련된 모든 상표, 서비스 마크, 로고 등에 관한 저작권 기타 지적재산권은 대한민국 및 외국의 법령에 기하여 서비스가<br /><i></i>보유하고 있거나 서비스에게 소유권 또는 사용권이 있습니다.</li>
    <li>3) 회원은 본 이용약관으로 인하여 서비스를 소유하거나 서비스에 관한 저작권을 보유하게 되는 것이 아니라, 서비스로부터 서비스의<br /><i></i>이용을 허락 받게 되는바, 서비스는 정보취득 또는 개인용도로만 제공되는 형태로 회원이 이용할 수 있습니다.</li>
    <li>4) 회원은 명시적으로 허락된 내용을 제외하고는 서비스를 통해 얻어지는 회원 상태정보를 영리 목적으로 사용, 복사, 유통하는 것을<br /><i></i>포함하여 서비스가 만든 텍스트, 스크립트, 그래픽의 회원 상호간 전송기능 등을 복사하거나 유통할 수 없습니다.</li>
    <li>5) 서비스는 서비스와 관련하여 회원에게 서비스가 정한 이용조건에 따라 계정, "아이디", 콘텐츠 등을 이용할 수 있는 이용권만을<br /><i></i>부여하며, 회원은 이를 양도, 판매, 담보제공 등의 처분행위를 할 수 없습니다.</li>
    <li>6) 서비스는 서비스와 관련하여 회원에게 서비스가 정한 이용조건에 따라 계정, ID, 콘텐츠 등을 이용할 수 있는 이용권만을 부여하며, <br /><i></i>이용자는 서비스를 이용함으로써 얻은 정보를 서비스의 사전 승낙 없이 복제, 송신, 출판, 배포, 방송 등 기타 방법에 의하여 영리 목적<br /><i></i>으로 이용하거나 제3자에게 이용하게 하여서는 안됩니다.</li>
</ul>
<br /><br />

<b>제 16 조 (회원에 대한 통지)</b><br />
<ul class="rule_list">
    <li>1) 서비스가 회원에 대한 통지를 하는 경우, 회원이 가입신청 시 서비스에 제출한 전자우편 주소나 SMS로 할 수 있습니다.</li>
    <li>2) 서비스는 불특정다수 회원에 대한 통지의 경우, 1주일 이상 사이트에 게시함으로써 개별 통지에 갈음할 수 있습니다.</li>
</ul>
<br /><br />

<b>제 17 조 (서비스의 의무)</b><br />
<ul class="rule_list">
    <li>1) 서비스는 관련법과 이 약관이 금지하거나 미풍양속에 반하는 행위를 하지 않으며, 계속적이고 안정적으로 서비스를 제공하기 위하여<br /><i></i>최선을 다하여 노력합니다.</li>
    <li>2) 서비스는 관계 법령이 정한 의무사항을 준수합니다.</li>
</ul>
<br /><br />

<b>제 18 조 (개별 서비스에 대한 약관 및 이용조건)</b><br />
서비스는 제공하는 서비스내의 개별 서비스에 대한 별도의 약관 및 이용조건을 둘 수 있으며 개별서비스에서 별도로 적용되는 약관에<br /><i></i>대한 동의는 회원이 개별서비스를 최초로 이용할 경우 별도의 동의절차를 거치게 됩니다. 이 경우 개별 서비스에 대한 이용약관 등이<br /><i></i>본 약관에 우선합니다.<br /><br />

<b>제 19 조 (서비스 이용시간)</b><br />
서비스의 이용은 서비스의 업무상 또는 기술상 특별한 지장이 없는 한 연중무휴 1일 24시간을 원칙으로 합니다. 다만 정기 점검 등의<br /><i></i>필요로 서비스가 정한 날이나 시간은 제외됩니다. 정기점검시간은 서비스제공화면에 공지한 바에 따릅니다.<br /><br />

<b>제 20 조 (서비스 제공의 변경)</b><br />
<ul class="rule_list">
    <li>1) 서비스는 이용 감소로 인한 원활한 서비스 제공의 곤란 및 수익성 악화, 기술 진보에 따른 차세대 서비스로의 전환 필요성, 서비스<br /><i></i>제공과 관련한 서비스 정책의 변경 등 기타 상당한 이유가 있는 경우에 운영상, 기술상의 필요에 따라 제공하고 있는 전부 또는<br /><i></i>일부 서비스를 변경 또는 중단할 수 있습니다.</li>
    <li>2) 서비스는 무료로 제공되는 서비스의 일부 또는 전부를 서비스의 정책 및 운영의 필요상 수정, 중단, 변경할 수 있으며, 이에 대하여<br /><i></i>관련법에 특별한 규정이 없는 한 회원에게 별도의 보상을 하지 않습니다.</li>
    <li>3) 서비스의 내용, 이용방법, 이용시간에 대하여 변경 또는 서비스 중단이 있는 경우에는 변경 또는 중단될 서비스의 내용 및 사유와<br /><i></i>일자 등은 그 변경 또는 중단 전에 서비스 "웹사이트" 또는 서비스 내 "공지사항" 화면 등 회원이 충분이 인지할 수 있는 방법으로<br /><i></i>30일의 기간을 두고 사전에 공지합니다.</li>
</ul>
<br /><br />

<b>제 21 조 (개인정보보호)</b><br />
<ul class="rule_list">
    <li>1) 서비스는 회원의 개인정보를 보호하기 위하여 정보통신망법 및 개인정보 보호법 등 관계 법령에서 정하는 바를 준수합니다.</li>
    <li>2) 서비스는 회원의 개인정보를 보호하기 위하여 개인정보취급방침을 제정, 서비스 화면에 게시합니다. 다만, 개인정보취급방침의<br /><i></i>구체적 내용은 연결화면을 통하여 볼 수 있습니다.</li>
    <li>3) 서비스는 개인정보취급방침에 따라 회원의 개인정보를 최대한 보호하기 위하여 노력합니다.</li>
    <li>4) 서비스의 공식 사이트 이외의 링크된 사이트에서는 서비스의 개인정보취급방침이 적용되지 않습니다. 링크된 사이트 및 서비스를<br /><i></i>제공하는 제3자의 개인정보 취급과 관련하여는 해당 사이트 및 제3자의 개인정보취급방침을 확인할 책임이 회원에게 있으며,<br /><i></i>서비스는 이에 대하여 책임을 부담하지 않습니다.</li>
    <li>5) 서비스는 다음과 같은 경우에 법이 허용하는 범위 내에서 회원의 개인정보를 제3자에게 제공할 수 있습니다.
        <ul>
            <li>(1) 수사기관이나 기타 정부기관으로부터 정보제공을 요청 받은 경우</li>
            <li>(2) 회원의 법령 또는 약관의 위반을 포함하여 부정행위 확인 등의 정보보호 업무를 위해 필요한 경우</li>
            <li>(3) 기타 법률에 의해 요구되는 경우</li>
        </ul>
    </li>
</ul>
<br /><br />

<b>제 22 조 (면책 조항)</b><br />
<ul class="rule_list">
    <li>1) 서비스는 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스 제공에 관한 책임이 면제됩니다.</li>
    <li>2) 서비스는 회원의 귀책사유로 인한 서비스 이용의 장애에 대하여 책임을 지지 않습니다.</li>
    <li>3) 서비스는 회원이 서비스를 이용하여 얻은 자료로 인한 손해에 관하여 책임을 지지 않습니다.</li>
    <li>4) 서비스는 회원이 게재한 정보, 자료, 사실의 신뢰도, 정확성 등 내용에 관해서는 책임을 지지 않습니다.</li>
    <li>5) 회원이 발송한 메일 내용에 대한 법적인 책임은 회원에게 있습니다.</li>
    <li>6) 서비스는 회원간 또는 회원과 제3자 상호간에 서비스를 매개로 하여 거래 등을 한 경우에는 책임이 면제됩니다.</li>
    <li>7) 서비스는 무료로 제공되는 서비스 이용과 관련하여 관련법에 특별한 규정이 없는 한 책임을 지지 않습니다.</li>
</ul>
<br /><br />

<b>제 23 조 (준거법 및 관할법원)</b><br />
<ul class="rule_list">
    <li>1) 이 약관의 해석 및 서비스와 회원간의 분쟁에 대하여는 대한민국의 법을 적용합니다.</li>
    <li>2) 서비스 이용 중 발생한 회원과 서비스간의 소송은 민사소송법에 의한 관할법원(서울중앙지방법원)에 제소합니다.</li>
</ul>
