<b>제 10 조 (회원, 이용자의 의무)</b><br />
<ul class="rule_list">
    <li>1) 회원은 관계법령이 약관의 규정, 이용안내 등 서비스가 통지하는 사항을 준수하여야 하며, 기타 서비스 업무에 방해되는 행위를<br /><i></i>하여서는 안됩니다.</li>
    <li>2) 회원은 서비스 이용과 관련하여 다음 각 호의 행위를 하여서는 안됩니다.
        <ul>
            <li>(1) 서비스 신청 또는 변경 시 허위내용의 등록</li>
            <li>(2) 서비스에 게시된 정보의 허가 받지 않은 변경</li>
            <li>(3) 서비스가 정한 정보 이외의 정보(컴퓨터 프로그램 등)의 송신 또는 게시</li>
            <li>(4) 서비스 기타 제3자의 저작권 등 지적 재산권에 대한 침해</li>
            <li>(5) 서비스 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위</li>
            <li>(6) 외설 또는 폭력적인 메시지, 화상, 음성 기타 공공질서 미풍양속에 반하는 정보를 서비스에 공개 또는 게시하는 행위</li>
            <li>(7) 기타 관계 법령이나 서비스에서 정한 규정에 위배되는 행위</li>
            <li>(8) 정당한 사유 없이 당사의 영업을 방해하는 내용을 기재하는 행위</li>
            <li>(9) 리버스엔지니어링, 디컴파일, 디스어셈블 및 기타 일체의 가공행위를 통하여 서비스를 복제, 분해 또는 모방 기타 변형하는 행위</li>
            <li>(10) 자동 접속 프로그램 등을 사용하는 등 정상적인 용법과 다른 방법으로 서비스를 이용하여 서비스의 서버에 부하를 일으켜<br /><i></i>정상적인 서비스를 방해하는 행위</li>
            <li>(11) 기타 관계법령에 위반된다고 판단되는 행위</li>
        </ul>
    </li>
</ul>
