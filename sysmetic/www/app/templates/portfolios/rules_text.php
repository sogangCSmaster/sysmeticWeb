 <b>제 6 조 (이용계약의 성립)</b><br />
<ul class="rule_list">
    <li>1) 이용계약은 회원이 되고자 하는 자(이하 "가입신청자")가 서비스가 정한 가입 양식에 따라 회원정보(전자우편주소, 비밀번호, 이름,<br /><i></i>연락처 등)를 기입하여 회원가입신청을 하고 서비스가 이러한 신청에 대하여 승인함으로써 체결됩니다.</li>
    <li>2) 서비스는 다음 각 호에 해당하는 신청에 대하여는 승인을 하지 않거나 사후에 이용계약을 해지할 수 있습니다.
        <ul>
            <li>(1) 가입신청자가 이 약관에 의하여 이전에 회원자격을 상실한 적이 있는 경우. 다만, 회원자격 상실 후 3개월이 경과한 자로서<br /><i></i>서비스의 회원 재가입 승낙을 얻은 경우에는 예외로 함</li>
            <li>(2) 타인의 명의를 이용한 경우</li>
            <li>(3) 서비스가 실명확인절차를 실시할 경우에 이용자의 실명가입신청이 사실 아님이 확인된 경우</li>
            <li>(4) 서비스에 의하여 이용계약이 해지된 날로부터3개월 이내에 재이용 신청을 하는 경우</li>
            <li>(5) 등록내용에 허위의 정보를 기재하거나, 기재누락, 오기가 있는 경우</li>
            <li>(6) 이미 가입된 회원과 전화번호나 전자우편주소가 동일한 경우</li>
            <li>(7) 부정한 용도 또는 영리를 추구할 목적으로 본 서비스를 이용하고자 하는 경우</li>
            <li>(8) 기타 이 약관에 위배되거나 위법 또는 부당한 이용신청임이 확인된 경우 및 서비스가 합리적인 판단에 의하여 필요하다고<br /><i></i>인정하는 경우</li>
            <li>(9) 관계법령에 위배되거나 사회의 안녕질서 혹은 미풍양속을 저해할 수 있는 목적으로 신청한 경우</li>
            <li>(10) 이용자의 귀책사유로 인하여 승인이 불가능하거나 기타 규정한 제반 사항을 위반하여 신청하는 경우</li>
        </ul>
    </li>
    <li>3) 제1항에 따른 신청에 있어 서비스는 회원의 종류에 따라 전문기관을 통한 실명확인 및 본인인증을 요청할 수 있습니다.</li>
    <li>4) 서비스는 서비스관련설비의 여유가 없거나, 기술상 또는 업무상 문제가 있는 경우에는 승낙을 유보할 수 있습니다.</li>
</ul>
