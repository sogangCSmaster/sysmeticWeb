<b> 제 1 조 (목적)</b><br />
이 약관은 시스메틱 트레이더 서비스(이하 "서비스"라 합니다)와 관련하여, 서비스와 이용고객(또는 회원)간에 서비스의 이용조건 및<br />절차, 서비스와 회원간의 권리, 의무 및 기타 필요한 사항을 규정함을 목적으로 합니다.<br /><br />

<b>제 2 조 (용어의 정의)</b><br />
<ul class="rule_list">
    <li>1) "회원" 이라 함은 서비스에 개인정보를 제공하여 회원등록을 한 자로서, 사이트에 전자우편주소를 등록하고 사이트의 정보를<br /><i></i>지속적으로 제공받으며, 사이트가 제공하는 서비스를 계속적으로 이용할 수 있는 자를 말합니다.</li>
    <li>2) "비회원" 이라 함은 회원으로 가입하지 않고 제공하는 서비스를 이용하는 자를 말합니다.</li>
    <li>3) "비밀번호(Password)"라 함은 회원의 동일성 확인과 회원의 권익 및 비밀보호를 위하여 회원 스스로가 설정하여 사이트에 등록한<br /><i></i>영문과 숫자의 조합을 말합니다.</li>
    <li>4) "게시물"이라 함은 회원이 서비스를 이용함에 있어 서비스 상에 게시한 부호ㆍ문자 등의 정보 형태의 글을 의미합니다.</li>
</ul>
<br /><br />

<b>제 3 조 (약관의 명시와 설명 및 개정)</b><br />
<ul class="rule_list">
    <li>1) 서비스는 이 약관의 내용을 이용자가 쉽게 알 수 있도록 서비스 화면에 게시합니다. 다만, 약관의 구체적 내용은 이용자가 연결화면을<br /><i></i>통하여 볼 수 있습니다.</li>
    <li>2) 서비스는 『약관의 규제 등에 관한 법률』,『전자서명법』, 『정보통신망 이용촉진 및 정보보호 등에 관한 법률』(정보통신망법) 등<br /><i></i>관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다.</li>
    <li>3) 서비스가 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행 약관과 함께 사이트의 공지사항에 그 개정약관의 적용일자<br /><i></i>30일 전부터 적용일자 전일까지 공지합니다. 다만, 회원에게 불리한 약관의 개정의 경우에는 공지 외에 일정기간 서비스 내 전자우편,<br /><i></i>로그인시 동의창 등의 전자적 수단을 통하여 따로 명확히 통지하도록 합니다.</li>
