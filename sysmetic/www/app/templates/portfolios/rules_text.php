<b>제 9 조 (회원의 ID 및 비밀번호에 대한 의무)</b><br />
<ul class="rule_list">
    <li>1) ID와 비밀번호에 관한 관리책임은 회원에게 있습니다.</li>
    <li>2) 회원은 자신의ID 및 비밀번호를 제3자에게 이용하게 해서는 안됩니다.</li>
    <li>3) 회원이 자신의ID 및 비밀번호를 도난 당하거나 제3자가 사용하고 있음을 인지한 경우에는 즉시 서비스에 통보하고 서비스의 조치가<br /><i></i>있는 경우에는 그에 따라야 합니다.</li>
    <li>4) 회원이 제3항에 따른 통지를 하지 않거나 서비스의 조치에 응하지 아니하여 발생하는 모든 불이익에 대한 책임은 회원에게 있습니다.</li>
</ul>
