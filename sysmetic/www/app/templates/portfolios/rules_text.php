    <li>2) 서비스는 전항에 따른 권리자의 요청이 없는 경우라도 권리침해가 인정될 만한 사유가 있거나 기타 서비스 정책 및 관련법에 위반되는<br /><i></i>경우에는 관련법에 따라 해당 "게시물"에 대해 임시조치 등을 취할 수 있습니다.</li>
    <li>3) 본 조에 따른 세부절차는 정보통신망법 및 저작권법이 규정한 범위 내에서 서비스가 정한 게시중단요청서비스에 따릅니다.<br />
    <i></i>- 게시중단요청 : help@sysmetictraders.com</li>
</ul>
